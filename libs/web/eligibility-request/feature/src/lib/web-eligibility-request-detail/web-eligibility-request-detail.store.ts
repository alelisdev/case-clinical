
import { Injectable } from '@angular/core'
import { ActivatedRoute,Router } from '@angular/router'
import { ComponentStore,tapResponse } from '@ngrx/component-store'
import { WebCoreDataAccessService,EligibilityRequest } from '@case-clinical/web/core/data-access'
import { pluck,switchMap, tap } from 'rxjs/operators'
import { DescriptionListItem } from '@case-clinical/web/ui/description-list'
import { WebUiToastService } from '@case-clinical/web/ui/toast'

export interface EligibilityRequestDetailState {
  errors ?: any
  loading?: boolean
  item?: EligibilityRequest
}

@Injectable()
export class WebEligibilityRequestDetailStore extends ComponentStore<EligibilityRequestDetailState> {
  constructor(
    private readonly data: WebCoreDataAccessService,
    private readonly router: Router,
    private readonly route: ActivatedRoute,
    private readonly toast: WebUiToastService
) {
    super({ loading: false })
    this.loadEligibilityRequestEffect(route.params.pipe(pluck('eligibilityRequestId')))
  }

  readonly errors$ = this.select((s) => s.errors)
  readonly loading$ = this.select((s) => s.loading)
  readonly item$ = this.select((s) => s.item)

readonly displayItems$ = this.select(
    this.item$,
    (item) =>
      [
{ label: 'Id', value: item?.id },
{ label: 'Name', value: item?.name },




{ label: 'Tax ID', value: item?.taxID },
{ label: 'Date of Birth', value: item?.dateOfBirth },
{ label: 'Member Registration Number', value: item?.memberRegistrationNumber },

      ] as DescriptionListItem[],
  )

readonly tabs$ = this.select(this.item$, (item) => [
    {
      label: 'Details',
      path: 'details',
      data: item,
    },
  ])
  readonly vm$ = this.select(this.errors$, this.loading$, this.item$, this.tabs$, (errors, loading, item, tabs) => ({
    errors,
    loading,
    item: { ...item },
    tabs
  }),
{debounce: true})

  readonly loadEligibilityRequestEffect = this.effect<string>((eligibilityRequestId$) =>
    eligibilityRequestId$.pipe(
      tap(() => this.setState({ loading: true })),
      switchMap((eligibilityRequestId) =>
        this.data.userEligibilityRequest({ eligibilityRequestId }).pipe(
          tapResponse(
            (res) => this.patchState({ item: res.data.item, errors: res.errors, loading: false }),
            (errors: any) =>
              this.patchState({
                loading: false,
                errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
              }),
          ),
        ),
      ),
    ),
  )

  readonly deleteEligibilityRequestEffect = this.effect<EligibilityRequest>(
    (eligibilityRequest$) =>
      eligibilityRequest$.pipe(
        switchMap((eligibilityRequest) =>
          this.data
            .userDeleteEligibilityRequest({
              eligibilityRequestId: eligibilityRequest.id,
            })
            .pipe(
              tapResponse(
                (res) =>
                {  
                    this.toast.success("Deleted successfully!", {duration: 3000})
                    return this.router.navigate(['/queues/eligibility-requests'])
                },
                (errors: any) =>
                  this.patchState({
                    loading: false,
                    errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
                  }),
              ),
            ),
        ),
      ),
  )
}

