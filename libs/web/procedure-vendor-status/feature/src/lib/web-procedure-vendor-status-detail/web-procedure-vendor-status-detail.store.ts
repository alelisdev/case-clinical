
import { Injectable } from '@angular/core'
import { ActivatedRoute,Router } from '@angular/router'
import { ComponentStore,tapResponse } from '@ngrx/component-store'
import { WebCoreDataAccessService,ProcedureVendorStatus } from '@case-clinical/web/core/data-access'
import { pluck,switchMap, tap } from 'rxjs/operators'
import { DescriptionListItem } from '@case-clinical/web/ui/description-list'
import { WebUiToastService } from '@case-clinical/web/ui/toast'

export interface ProcedureVendorStatusDetailState {
  errors ?: any
  loading?: boolean
  item?: ProcedureVendorStatus
}

@Injectable()
export class WebProcedureVendorStatusDetailStore extends ComponentStore<ProcedureVendorStatusDetailState> {
  constructor(
    private readonly data: WebCoreDataAccessService,
    private readonly router: Router,
    private readonly route: ActivatedRoute,
    private readonly toast: WebUiToastService
) {
    super({ loading: false })
    this.loadProcedureVendorStatusEffect(route.params.pipe(pluck('procedureVendorStatusId')))
  }

  readonly errors$ = this.select((s) => s.errors)
  readonly loading$ = this.select((s) => s.loading)
  readonly item$ = this.select((s) => s.item)

readonly displayItems$ = this.select(
    this.item$,
    (item) =>
      [
{ label: 'Id', value: item?.id },
{ label: 'Name', value: item?.name },
{ label: 'Procedure Vendors', value: item?.procedureVendors },
      ] as DescriptionListItem[],
  )

readonly tabs$ = this.select(this.item$, (item) => [
    {
      label: 'Details',
      path: 'details',
      data: item,
    },
  ])
  readonly vm$ = this.select(this.errors$, this.loading$, this.item$, this.tabs$, (errors, loading, item, tabs) => ({
    errors,
    loading,
    item: { ...item },
    tabs
  }),
{debounce: true})

  readonly loadProcedureVendorStatusEffect = this.effect<string>((procedureVendorStatusId$) =>
    procedureVendorStatusId$.pipe(
      tap(() => this.setState({ loading: true })),
      switchMap((procedureVendorStatusId) =>
        this.data.userProcedureVendorStatus({ procedureVendorStatusId }).pipe(
          tapResponse(
            (res) => this.patchState({ item: res.data.item, errors: res.errors, loading: false }),
            (errors: any) =>
              this.patchState({
                loading: false,
                errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
              }),
          ),
        ),
      ),
    ),
  )

  readonly deleteProcedureVendorStatusEffect = this.effect<ProcedureVendorStatus>(
    (procedureVendorStatus$) =>
      procedureVendorStatus$.pipe(
        switchMap((procedureVendorStatus) =>
          this.data
            .userDeleteProcedureVendorStatus({
              procedureVendorStatusId: procedureVendorStatus.id,
            })
            .pipe(
              tapResponse(
                (res) =>
                {  
                    this.toast.success("Deleted successfully!", {duration: 3000})
                    return this.router.navigate(['/queues/procedure-vendor-statuses'])
                },
                (errors: any) =>
                  this.patchState({
                    loading: false,
                    errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
                  }),
              ),
            ),
        ),
      ),
  )
}

