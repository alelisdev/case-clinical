
import { Injectable } from '@angular/core'
import { ActivatedRoute,Router } from '@angular/router'
import { ComponentStore,tapResponse } from '@ngrx/component-store'
import { WebCoreDataAccessService,ContractedRate } from '@case-clinical/web/core/data-access'
import { pluck,switchMap, tap } from 'rxjs/operators'
import { DescriptionListItem } from '@case-clinical/web/ui/description-list'
import { WebUiToastService } from '@case-clinical/web/ui/toast'

export interface ContractedRateDetailState {
  errors ?: any
  loading?: boolean
  item?: ContractedRate
}

@Injectable()
export class WebContractedRateDetailStore extends ComponentStore<ContractedRateDetailState> {
  constructor(
    private readonly data: WebCoreDataAccessService,
    private readonly router: Router,
    private readonly route: ActivatedRoute,
    private readonly toast: WebUiToastService
) {
    super({ loading: false })
    this.loadContractedRateEffect(route.params.pipe(pluck('contractedRateId')))
  }

  readonly errors$ = this.select((s) => s.errors)
  readonly loading$ = this.select((s) => s.loading)
  readonly item$ = this.select((s) => s.item)

readonly displayItems$ = this.select(
    this.item$,
    (item) =>
      [
{ label: 'Id', value: item?.id },
{ label: 'Name', value: item?.name },
{ label: 'Amount', value: item?.amount },
{ label: 'Percentage', value: item?.percentage },
{ label: 'Reimbursed Rate', value: item?.reimbursedRate },
{ label: 'Bill on Behalf', value: item?.billOnBehalf },






      ] as DescriptionListItem[],
  )

readonly tabs$ = this.select(this.item$, (item) => [
    {
      label: 'Details',
      path: 'details',
      data: item,
    },
  ])
  readonly vm$ = this.select(this.errors$, this.loading$, this.item$, this.tabs$, (errors, loading, item, tabs) => ({
    errors,
    loading,
    item: { ...item },
    tabs
  }),
{debounce: true})

  readonly loadContractedRateEffect = this.effect<string>((contractedRateId$) =>
    contractedRateId$.pipe(
      tap(() => this.setState({ loading: true })),
      switchMap((contractedRateId) =>
        this.data.userContractedRate({ contractedRateId }).pipe(
          tapResponse(
            (res) => this.patchState({ item: res.data.item, errors: res.errors, loading: false }),
            (errors: any) =>
              this.patchState({
                loading: false,
                errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
              }),
          ),
        ),
      ),
    ),
  )

  readonly deleteContractedRateEffect = this.effect<ContractedRate>(
    (contractedRate$) =>
      contractedRate$.pipe(
        switchMap((contractedRate) =>
          this.data
            .userDeleteContractedRate({
              contractedRateId: contractedRate.id,
            })
            .pipe(
              tapResponse(
                (res) =>
                {  
                    this.toast.success("Deleted successfully!", {duration: 3000})
                    return this.router.navigate(['/queues/contracted-rates'])
                },
                (errors: any) =>
                  this.patchState({
                    loading: false,
                    errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
                  }),
              ),
            ),
        ),
      ),
  )
}

