
import { Injectable } from '@angular/core'
import { ActivatedRoute,Router } from '@angular/router'
import { ComponentStore,tapResponse } from '@ngrx/component-store'
import { WebCoreDataAccessService,MedLevel } from '@case-clinical/web/core/data-access'
import { pluck,switchMap, tap } from 'rxjs/operators'
import { DescriptionListItem } from '@case-clinical/web/ui/description-list'
import { WebUiToastService } from '@case-clinical/web/ui/toast'

export interface MedLevelDetailState {
  errors ?: any
  loading?: boolean
  item?: MedLevel
}

@Injectable()
export class WebMedLevelDetailStore extends ComponentStore<MedLevelDetailState> {
  constructor(
    private readonly data: WebCoreDataAccessService,
    private readonly router: Router,
    private readonly route: ActivatedRoute,
    private readonly toast: WebUiToastService
) {
    super({ loading: false })
    this.loadMedLevelEffect(route.params.pipe(pluck('medLevelId')))
  }

  readonly errors$ = this.select((s) => s.errors)
  readonly loading$ = this.select((s) => s.loading)
  readonly item$ = this.select((s) => s.item)

readonly displayItems$ = this.select(
    this.item$,
    (item) =>
      [
{ label: 'Id', value: item?.id },
{ label: 'Name', value: item?.name },
{ label: 'Approved Site Costs', value: item?.approvedSiteCosts },
{ label: 'Maximum Medical Bills to Date', value: item?.maximumMedicalBillsToDate },
{ label: 'Legal Cases', value: item?.legalCases },
{ label: 'Require Fields', value: item?.requireFields },
      ] as DescriptionListItem[],
  )

readonly tabs$ = this.select(this.item$, (item) => [
    {
      label: 'Details',
      path: 'details',
      data: item,
    },
  ])
  readonly vm$ = this.select(this.errors$, this.loading$, this.item$, this.tabs$, (errors, loading, item, tabs) => ({
    errors,
    loading,
    item: { ...item },
    tabs
  }),
{debounce: true})

  readonly loadMedLevelEffect = this.effect<string>((medLevelId$) =>
    medLevelId$.pipe(
      tap(() => this.setState({ loading: true })),
      switchMap((medLevelId) =>
        this.data.userMedLevel({ medLevelId }).pipe(
          tapResponse(
            (res) => this.patchState({ item: res.data.item, errors: res.errors, loading: false }),
            (errors: any) =>
              this.patchState({
                loading: false,
                errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
              }),
          ),
        ),
      ),
    ),
  )

  readonly deleteMedLevelEffect = this.effect<MedLevel>(
    (medLevel$) =>
      medLevel$.pipe(
        switchMap((medLevel) =>
          this.data
            .userDeleteMedLevel({
              medLevelId: medLevel.id,
            })
            .pipe(
              tapResponse(
                (res) =>
                {  
                    this.toast.success("Deleted successfully!", {duration: 3000})
                    return this.router.navigate(['/queues/med-levels'])
                },
                (errors: any) =>
                  this.patchState({
                    loading: false,
                    errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
                  }),
              ),
            ),
        ),
      ),
  )
}

