
import { Injectable } from '@angular/core'
import { ActivatedRoute,Router } from '@angular/router'
import { ComponentStore,tapResponse } from '@ngrx/component-store'
import { WebCoreDataAccessService,Specialty } from '@case-clinical/web/core/data-access'
import { pluck,switchMap, tap } from 'rxjs/operators'
import { DescriptionListItem } from '@case-clinical/web/ui/description-list'
import { WebUiToastService } from '@case-clinical/web/ui/toast'

export interface SpecialtyDetailState {
  errors ?: any
  loading?: boolean
  item?: Specialty
}

@Injectable()
export class WebSpecialtyDetailStore extends ComponentStore<SpecialtyDetailState> {
  constructor(
    private readonly data: WebCoreDataAccessService,
    private readonly router: Router,
    private readonly route: ActivatedRoute,
    private readonly toast: WebUiToastService
) {
    super({ loading: false })
    this.loadSpecialtyEffect(route.params.pipe(pluck('specialtyId')))
  }

  readonly errors$ = this.select((s) => s.errors)
  readonly loading$ = this.select((s) => s.loading)
  readonly item$ = this.select((s) => s.item)

readonly displayItems$ = this.select(
    this.item$,
    (item) =>
      [
{ label: 'Id', value: item?.id },
{ label: 'Name', value: item?.name },
{ label: 'Active', value: item?.active },
{ label: 'Fee Schedules', value: item?.feeSchedules },
{ label: 'Clinical Provider Specialties', value: item?.clinicalProviderSpecialties },
{ label: 'Facility Fee Schedules', value: item?.facilityFeeSchedules },
      ] as DescriptionListItem[],
  )

readonly tabs$ = this.select(this.item$, (item) => [
    {
      label: 'Details',
      path: 'details',
      data: item,
    },
  ])
  readonly vm$ = this.select(this.errors$, this.loading$, this.item$, this.tabs$, (errors, loading, item, tabs) => ({
    errors,
    loading,
    item: { ...item },
    tabs
  }),
{debounce: true})

  readonly loadSpecialtyEffect = this.effect<string>((specialtyId$) =>
    specialtyId$.pipe(
      tap(() => this.setState({ loading: true })),
      switchMap((specialtyId) =>
        this.data.userSpecialty({ specialtyId }).pipe(
          tapResponse(
            (res) => this.patchState({ item: res.data.item, errors: res.errors, loading: false }),
            (errors: any) =>
              this.patchState({
                loading: false,
                errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
              }),
          ),
        ),
      ),
    ),
  )

  readonly deleteSpecialtyEffect = this.effect<Specialty>(
    (specialty$) =>
      specialty$.pipe(
        switchMap((specialty) =>
          this.data
            .userDeleteSpecialty({
              specialtyId: specialty.id,
            })
            .pipe(
              tapResponse(
                (res) =>
                {  
                    this.toast.success("Deleted successfully!", {duration: 3000})
                    return this.router.navigate(['/queues/specialties'])
                },
                (errors: any) =>
                  this.patchState({
                    loading: false,
                    errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
                  }),
              ),
            ),
        ),
      ),
  )
}

