
import { Injectable } from '@angular/core'
import { ActivatedRoute,Router } from '@angular/router'
import { ComponentStore,tapResponse } from '@ngrx/component-store'
import { WebCoreDataAccessService,BalanceRequest } from '@case-clinical/web/core/data-access'
import { pluck,switchMap, tap } from 'rxjs/operators'
import { DescriptionListItem } from '@case-clinical/web/ui/description-list'
import { WebUiToastService } from '@case-clinical/web/ui/toast'

export interface BalanceRequestDetailState {
  errors ?: any
  loading?: boolean
  item?: BalanceRequest
}

@Injectable()
export class WebBalanceRequestDetailStore extends ComponentStore<BalanceRequestDetailState> {
  constructor(
    private readonly data: WebCoreDataAccessService,
    private readonly router: Router,
    private readonly route: ActivatedRoute,
    private readonly toast: WebUiToastService
) {
    super({ loading: false })
    this.loadBalanceRequestEffect(route.params.pipe(pluck('balanceRequestId')))
  }

  readonly errors$ = this.select((s) => s.errors)
  readonly loading$ = this.select((s) => s.loading)
  readonly item$ = this.select((s) => s.item)

readonly displayItems$ = this.select(
    this.item$,
    (item) =>
      [
{ label: 'Id', value: item?.id },
{ label: 'Name', value: item?.name },
{ label: 'Requested on', value: item?.requestedOn },
{ label: 'Replied on', value: item?.repliedOn },
{ label: 'Status', value: item?.status },

{ label: 'Type', value: item?.type },
{ label: 'Balance Amount', value: item?.balanceAmount },

      ] as DescriptionListItem[],
  )

readonly tabs$ = this.select(this.item$, (item) => [
    {
      label: 'Details',
      path: 'details',
      data: item,
    },
  ])
  readonly vm$ = this.select(this.errors$, this.loading$, this.item$, this.tabs$, (errors, loading, item, tabs) => ({
    errors,
    loading,
    item: { ...item },
    tabs
  }),
{debounce: true})

  readonly loadBalanceRequestEffect = this.effect<string>((balanceRequestId$) =>
    balanceRequestId$.pipe(
      tap(() => this.setState({ loading: true })),
      switchMap((balanceRequestId) =>
        this.data.userBalanceRequest({ balanceRequestId }).pipe(
          tapResponse(
            (res) => this.patchState({ item: res.data.item, errors: res.errors, loading: false }),
            (errors: any) =>
              this.patchState({
                loading: false,
                errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
              }),
          ),
        ),
      ),
    ),
  )

  readonly deleteBalanceRequestEffect = this.effect<BalanceRequest>(
    (balanceRequest$) =>
      balanceRequest$.pipe(
        switchMap((balanceRequest) =>
          this.data
            .userDeleteBalanceRequest({
              balanceRequestId: balanceRequest.id,
            })
            .pipe(
              tapResponse(
                (res) =>
                {  
                    this.toast.success("Deleted successfully!", {duration: 3000})
                    return this.router.navigate(['/queues/balance-requests'])
                },
                (errors: any) =>
                  this.patchState({
                    loading: false,
                    errors: errors.graphQLErrors ? errors.graphQLErrors : errors,
                  }),
              ),
            ),
        ),
      ),
  )
}

