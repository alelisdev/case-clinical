test('UserFirmStatus Query Test', async ({ page }) => {
  const firmStatusesQuery = `
    query UserFirmStatuses {
      items: userFirmStatuses {
        id
      }
    }
  `;

  // Send a GraphQL query to retrieve user accident types
  const graphqlUrl = process.env.GRAPHQL_URL || '';
  const url = new URL(graphqlUrl);
  const firmStatusesResponse = await sendGraphQLQuery(url.href,firmStatusesQuery);

  // Verify the response data
  const { data: { items: firmStatuses }, errors: firmStatusesErrors } = firmStatusesResponse;
  expect(firmStatusesErrors).toBeUndefined();
  expect(firmStatuses).toBeDefined();
  expect(firmStatuses.length).toBeGreaterThan(0);

  // Use the first accident type ID for the subsequent user accident type query
  const firmStatusId = firmStatuses[0].id;

  const query = `
    query UserFirmStatus($firmStatusId: String!) {
      item: userFirmStatus(firmStatusId: $firmStatusId) {
id
createdAt
updatedAt
name
blackListed
active
statusColor






firms {
    id
    name
  }
      }
    }
  `;

  // Send a GraphQL query to retrieve the user accident type using the accident type ID
  const response = await sendGraphQLQuery(graphqlUrl, query, { firmStatusId });

  // Verify the response data
  const { data, errors } = response;
  expect(errors).toBeUndefined();
  expect(data).toBeDefined();

  const { item } = data;
  expect(item).toBeDefined();
});


