test('UserBatchControl Query Test', async ({ page }) => {
  const batchControlsQuery = `
    query UserBatchControls {
      items: userBatchControls {
        id
      }
    }
  `;

  // Send a GraphQL query to retrieve user accident types
  const graphqlUrl = process.env.GRAPHQL_URL || '';
  const url = new URL(graphqlUrl);
  const batchControlsResponse = await sendGraphQLQuery(url.href,batchControlsQuery);

  // Verify the response data
  const { data: { items: batchControls }, errors: batchControlsErrors } = batchControlsResponse;
  expect(batchControlsErrors).toBeUndefined();
  expect(batchControls).toBeDefined();
  expect(batchControls.length).toBeGreaterThan(0);

  // Use the first accident type ID for the subsequent user accident type query
  const batchControlId = batchControls[0].id;

  const query = `
    query UserBatchControl($batchControlId: String!) {
      item: userBatchControl(batchControlId: $batchControlId) {
id
createdAt
updatedAt
name
trackingNumber
batchTotal
defaultGLCode
posted






payments {
    id
    name
  }
      }
    }
  `;

  // Send a GraphQL query to retrieve the user accident type using the accident type ID
  const response = await sendGraphQLQuery(graphqlUrl, query, { batchControlId });

  // Verify the response data
  const { data, errors } = response;
  expect(errors).toBeUndefined();
  expect(data).toBeDefined();

  const { item } = data;
  expect(item).toBeDefined();
});


