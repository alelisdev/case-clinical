test('UserContactTag Query Test', async ({ page }) => {
  const contactTagsQuery = `
    query UserContactTags {
      items: userContactTags {
        id
      }
    }
  `;

  // Send a GraphQL query to retrieve user accident types
  const graphqlUrl = process.env.GRAPHQL_URL || '';
  const url = new URL(graphqlUrl);
  const contactTagsResponse = await sendGraphQLQuery(url.href,contactTagsQuery);

  // Verify the response data
  const { data: { items: contactTags }, errors: contactTagsErrors } = contactTagsResponse;
  expect(contactTagsErrors).toBeUndefined();
  expect(contactTags).toBeDefined();
  expect(contactTags.length).toBeGreaterThan(0);

  // Use the first accident type ID for the subsequent user accident type query
  const contactTagId = contactTags[0].id;

  const query = `
    query UserContactTag($contactTagId: String!) {
      item: userContactTag(contactTagId: $contactTagId) {
id
createdAt
updatedAt
name
contactId
contact {
  id
  name
}





      }
    }
  `;

  // Send a GraphQL query to retrieve the user accident type using the accident type ID
  const response = await sendGraphQLQuery(graphqlUrl, query, { contactTagId });

  // Verify the response data
  const { data, errors } = response;
  expect(errors).toBeUndefined();
  expect(data).toBeDefined();

  const { item } = data;
  expect(item).toBeDefined();
});


