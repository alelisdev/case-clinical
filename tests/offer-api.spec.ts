test('UserOffer Query Test', async ({ page }) => {
  const offersQuery = gql`
    query UserOffers {
      items: userOffers {
        id
      }
    }
  `;

  // Send a GraphQL query to retrieve user accident types
  const graphqlUrl = process.env.GRAPHQL_URL || '';
  const url = new URL(graphqlUrl);
  const offersResponse = await sendGraphQLQuery(url.href,offersQuery);

  // Verify the response data
  const { data: { items: offers }, errors: offersErrors } = offersResponse;
  expect(offersErrors).toBeUndefined();
  expect(offers).toBeDefined();
  expect(offers.length).toBeGreaterThan(0);

  // Use the first accident type ID for the subsequent user accident type query
  const offerId = offers[0].id;

  const query = gql`
    query UserOffer($offerId: String!) {
      item: userOffer(offerId: $offerId) {
id
createdAt
updatedAt
name
amount
invoiceId






      }
    }
  `;

  // Send a GraphQL query to retrieve the user accident type using the accident type ID
  const response = await sendGraphQLQuery(graphqlUrl, query, { offerId });

  // Verify the response data
  const { data, errors } = response;
  expect(errors).toBeUndefined();
  expect(data).toBeDefined();

  const { item } = data;
  expect(item).toBeDefined();
});


