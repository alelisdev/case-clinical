test('UserIntegration Query Test', async ({ page }) => {
  const integrationsQuery = `
    query UserIntegrations {
      items: userIntegrations {
        id
      }
    }
  `;

  // Send a GraphQL query to retrieve user accident types
  const graphqlUrl = process.env.GRAPHQL_URL || '';
  const url = new URL(graphqlUrl);
  const integrationsResponse = await sendGraphQLQuery(url.href,integrationsQuery);

  // Verify the response data
  const { data: { items: integrations }, errors: integrationsErrors } = integrationsResponse;
  expect(integrationsErrors).toBeUndefined();
  expect(integrations).toBeDefined();
  expect(integrations.length).toBeGreaterThan(0);

  // Use the first accident type ID for the subsequent user accident type query
  const integrationId = integrations[0].id;

  const query = `
    query UserIntegration($integrationId: String!) {
      item: userIntegration(integrationId: $integrationId) {
id
createdAt
updatedAt
name






contactSettings {
    id
    name
  }
      }
    }
  `;

  // Send a GraphQL query to retrieve the user accident type using the accident type ID
  const response = await sendGraphQLQuery(graphqlUrl, query, { integrationId });

  // Verify the response data
  const { data, errors } = response;
  expect(errors).toBeUndefined();
  expect(data).toBeDefined();

  const { item } = data;
  expect(item).toBeDefined();
});


